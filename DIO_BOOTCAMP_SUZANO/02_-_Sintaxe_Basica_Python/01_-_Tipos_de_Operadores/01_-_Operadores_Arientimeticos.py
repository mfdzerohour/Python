#Procedencia Matematica
# A definição indica a seguinte ordem como a correta:
# 1 - Parenteses
# 2 - Expoêntes
# 3 - Multiplicação e divisões (da esquerda para que direita)
# 4 - Adição e subtração (da esquerda para que direita)

print (10 - 5 * 2) #Ordem da matematica
# 0

print ((10 - 5) * 2) #Parenteses
# 8 

